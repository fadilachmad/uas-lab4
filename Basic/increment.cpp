#include <iostream>
using namespace std;
int main ()
{
    int in;
    in = 10;
    cout << "nilai awal : " << in << endl;
    cout << "nilai ++in : " << ++in << endl;
    cout << "nilai hasil : " << in << endl;

    in = 10;
    cout << "nilai awal : " << in << endl;
    cout << "nilai ++in : " << in++ << endl;
    cout << "nilai hasil : " << in << endl;
}